    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String email = jTextField3.getText();
        Date date = jDateChooser1.getDate();
        String gender = String.valueOf(jComboBox1.getSelectedIndex());
        String address = jTextField6.getText();

        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String formaDate = format.format(date);

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Name", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Email", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (date == null) {
            JOptionPane.showMessageDialog(this, "Invalid Date", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (gender == null) {
            JOptionPane.showMessageDialog(this, "Invalid Gender", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Address", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                MySQL.execute("INSERT INTO student(NAME, date_of_birth, address, email, mobile, gender_id)\n"
                        + "VALUES('" + name + "', '" + formaDate + "', '" + address + "', '" + email + "', '" + mobile + "', '" + gender + "')");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

