    private void loadClasses() {
        String query = "SELECT class.*, teacher.name AS teacher, `subject`.name AS subject FROM class \n"
                + "INNER JOIN teacher ON class.teachers_id = teacher.id \n"
                + "INNER JOIN `subject` ON `subject`.id = class.subject_id";
        try {
            ResultSet classes = MySQL.execute(query);
            Vector<String> items = new Vector<>();
            items.add("Select");

            int index = 1;
            while (classes.next()) {
                items.add(classes.getString("teacher") + " - " + classes.getString("subject"));
                classMap.put(index, classes.getInt("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(items);
            jComboBox1.setModel(model);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void loadPayment() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String query = "SELECT payment.*, student.name AS student, class.id AS class FROM payment \n"
                + "INNER JOIN student ON payment.student_id = student.id\n"
                + "INNER JOIN class ON class.id = payment.class_id";
        
        try {
            ResultSet result = MySQL.execute(query);
            while(result.next()) {
                Vector v = new Vector();
                v.add(result.getString("id"));
                v.add(result.getString("date"));
                v.add(result.getString("student"));
                int classs = result.getInt("class");
                jComboBox1.setSelectedIndex(classs);
                
                v.add(String.valueOf(jComboBox1.getSelectedItem()));
                
                model.addRow(v);
            }
            jComboBox1.setSelectedIndex(0);
        } catch (Exception ex) {
            Logger.getLogger(manage_payment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

